package Classes;

public class Servico {
    
    private int ser_Cod;
    private String ser_Des;
    private double ser_Val;
    private String ser_Tip;
    private String ser_Sta;     

    public int getSer_Cod() {
        return ser_Cod;
    }

    public void setSer_Cod(int ser_Cod) {
        this.ser_Cod = ser_Cod;
    }

    public String getSer_Des() {
        return ser_Des;
    }

    public void setSer_Des(String ser_Des) {
        this.ser_Des = ser_Des;
    }

    public double getSer_Val() {
        return ser_Val;
    }

    public void setSer_Val(double ser_Val) {
        this.ser_Val = ser_Val;
    }

    public String getSer_Tip() {
        return ser_Tip;
    }

    public void setSer_Tip(String ser_Tip) {
        this.ser_Tip = ser_Tip;
    }

    public String getSer_Sta() {
        return ser_Sta;
    }

    public void setSer_Sta(String ser_Sta) {
        this.ser_Sta = ser_Sta;
    }       
    
}
