    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        if (txtNome.equals("") && txtLogin.equals("")) {
            UsuarioDAO.listarUsuario("", "");
        } else {
            if (txtNome.equals("")) {
                UsuarioDAO.listarUsuario("", txtLogin.getText());
            } else {
                if (txtLogin.equals("")) {
                    UsuarioDAO.listarUsuario(txtNome.getText(), "");
                } else {
                    UsuarioDAO.listarUsuario(txtNome.getText(), txtLogin.getText());
                }
            }
        }
        dimensionarTabela();
    }//GEN-LAST:event_btnPesquisarActionPerformed

