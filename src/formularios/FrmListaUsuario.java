    private void preencherTabela() {
        if (tblUsuario.getSelectedRow() > -1) {
            tblUsuario.removeRowSelectionInterval(tblUsuario.getSelectedRow(), tblUsuario.getSelectedRow());
        }
        txtNome.setText("");
        txtLogin.setText("");
        UsuarioDAO.listarUsuario("", "");
        dimensionarTabela();
    }

    private void atualizar() {
        if (tblUsuario.getRowCount() > 0) {
            if (tblUsuario.getSelectedRow() > -1) {
                int linha = tblUsuario.getSelectedRow();
                int id = Integer.parseInt(tblUsuario.getValueAt(linha, 0).toString());

                FrmUsuario usuario = new FrmUsuario(id);
                frmMenuPrincipal.JDesktopPanePrincipall.add(usuario);
                usuario.setVisible(true);
                usuario.setPosicao();
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione o registro na tabela a ser alterado.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Não há registro na tabela para ser atualizado.");
        }
    }

    private void excluir() {
        if (tblUsuario.getRowCount() > 0) {
            if (tblUsuario.getSelectedRow() > -1) {
                int linha = tblUsuario.getSelectedRow();
                int id = Integer.parseInt(tblUsuario.getValueAt(linha, 0).toString());
                String nome = tblUsuario.getValueAt(linha, 2).toString();

                int ok = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja excluir"
                        + " usuário " + nome + "?", "Aviso", JOptionPane.YES_NO_OPTION);

                if (ok == 0) {
                    UsuarioDAO.excluir(id);
                    UsuarioDAO.listarUsuario("", "");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione o registro na tabela a ser excluído.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Não há registro na tabela para ser excluído.");
        }
    }

    private void dimensionarTabela() {
        int altura = 20 * tblUsuario.getRowCount();
        tblUsuario.setPreferredSize(new java.awt.Dimension(550, altura));
    }

    public void setPosicao() {
        Dimension d = this.getDesktopPane().getSize();
        this.setLocation((d.width - this.getSize().width) / 2, (d.height - this.getSize().height) / 2);
    }
