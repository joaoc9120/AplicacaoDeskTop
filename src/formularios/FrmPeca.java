    private void carregarProduto(int cod) {

        Peca produto = PecaDAO.carregarProduto(cod);

        txtDescricao.setText(produto.getPro_Des());
        txtQuantMin.setText(Integer.toString(produto.getPro_Qua_Min()));
        txtQuantEstoque.setText(Integer.toString(produto.getPro_Qua_Est()));       
        jtxtValorCusto.setText(TratarMoeda.FormatarMoeda(produto.getPro_Val_Cus()));
        jtxtValor.setText(TratarMoeda.FormatarMoeda(produto.getPro_Val_Ven()));  
    }

    private boolean camposObrigatorios(){
        boolean retorno = false;
        mensagem = "Preenchimento obrigatório\n do campo abaixo:\n\n";
        if (txtDescricao.getText().trim().equals("")) {
            mensagem += "Descrição;\n";
            retorno = true;
        }
        return retorno;
    }    
    
    public void setPosicao() {
        Dimension d = this.getDesktopPane().getSize();
        this.setLocation((d.width - this.getSize().width) / 2, (d.height - this.getSize().height) / 2);
    }

