    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (camposObrigatorios()) {
            JOptionPane.showMessageDialog(this, mensagem, "Alerta", 0,
                    new ImageIcon(getClass().getResource("/imagens/icons/info_P.png")));
        } else {
            Peca produto = new Peca();
            produto.setPro_Des(txtDescricao.getText().trim());
            produto.setPro_Tip(ServicoProduto.PRODUTO.toString());
            if(txtQuantMin.getText().trim().isEmpty()){
                produto.setPro_Qua_Min(0);
            }else{
                produto.setPro_Qua_Min(Integer.parseInt(txtQuantMin.getText()));
            }
            
            if(txtQuantEstoque.getText().trim().equals("")){
                produto.setPro_Qua_Est(0);
            }else{
                produto.setPro_Qua_Est(Integer.parseInt(txtQuantEstoque.getText()));
            }
            produto.setPro_Val_Cus(TratarMoeda.Moeda(jtxtValorCusto.getText()));
            produto.setPro_Val_Ven(TratarMoeda.Moeda(jtxtValor.getText()));
            produto.setPro_Sta(StatusRegistro.ATIVO.toString());

            if (this.idProduto > 0) {
                produto.setPro_Cod(this.idProduto);
                PecaDAO.alterarProduto(produto);
            } else {
                produto.setPro_Cod(gerarId.gerarIDProdutoServico());
                this.idProduto = produto.getPro_Cod();
                PecaDAO.gravarProduto(produto);
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

