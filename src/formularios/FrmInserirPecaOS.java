    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        if (txtPeca.equals("")) {
            OrdemServicoItemDAO.listarPeca("");
        } else {
            OrdemServicoItemDAO.listarPeca(txtPeca.getText().trim());
        }
        dimensionarTabela();
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        if (selecionado) {
            ItemOS itemos = new ItemOS();
            itemos.setOs_Cod(os_Cod);
            itemos.setOs_Item_Cod(gerarId.gerarIdOrdemServicoItem(os_Cod));
            itemos.setOs_Item_Prod_Cod(Integer.parseInt(codigoProduto));
            itemos.setOs_Item_Qua(Integer.parseInt(txtQuant.getText()));
            itemos.setOs_Item_Val(valorUnit);

            OrdemServicoItemDAO.gravarOrdemServicoItem(itemos);
            
            if(origem == "O"){
                OrdemServicoItemDAO.listarOrdemServicoItem(os_Cod, "O");                
            }
            else{
                OrdemServicoItemDAO.listarOrdemServicoItem(os_Cod, "S");
                FrmOrdemServico.dimensionarTabelaPeca();
            }            
            txtDescricao.setText("");
            txtQuant.setText("");
            selecionado = false;
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item com duplo click do mouse.");
        }
    }//GEN-LAST:event_btnInserirActionPerformed

