    private void tblPecaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPecaMouseClicked
        if (evt.getClickCount() == 2) {
            selecionado = true;
            valorUnit = 0;
            int linha = tblPeca.getSelectedRow();
            codigoProduto = tblPeca.getValueAt(linha, 0).toString();
            txtDescricao.setText(tblPeca.getValueAt(linha, 1).toString());
            txtQuant.setText("1");
            valorUnit = Double.parseDouble(tblPeca.getValueAt(linha, 3).toString());
        }
    }//GEN-LAST:event_tblPecaMouseClicked

    private void preencherTabela() {
        if (tblPeca.getSelectedRow() > -1) {
            tblPeca.removeRowSelectionInterval(tblPeca.getSelectedRow(), tblPeca.getSelectedRow());
        }
        txtPeca.setText("");
        OrdemServicoItemDAO.listarPeca("");
        dimensionarTabela();    
    }
   
    private void dimensionarTabela(){
        int altura = 20 * tblPeca.getRowCount();
        tblPeca.setPreferredSize(new java.awt.Dimension(610, altura));    
    }

