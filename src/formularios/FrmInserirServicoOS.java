    private void tblServicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblServicoMouseClicked
        if (evt.getClickCount() == 2) {
            selecionado = true;
            valorUnit = 0;
            int linha = tblServico.getSelectedRow();
            codigoProduto = tblServico.getValueAt(linha, 0).toString();
            txtDescricao.setText(tblServico.getValueAt(linha, 1).toString());
            txtQuant.setText("1");
            valorUnit = Double.parseDouble(tblServico.getValueAt(linha, 2).toString());
        }
    }//GEN-LAST:event_tblServicoMouseClicked

    public void limparCampos() {
        if (tblServico.getSelectedRow() > -1) {
            tblServico.removeRowSelectionInterval(tblServico.getSelectedRow(), tblServico.getSelectedRow());
        }
        txtPeca.setText("");
        OrdemServicoItemDAO.listarServico("");
        dimensionarTabela();
    }

    private void preencherTabela() {
        if (tblServico.getSelectedRow() > -1) {
            tblServico.removeRowSelectionInterval(tblServico.getSelectedRow(), tblServico.getSelectedRow());
        }
        txtPeca.setText("");
        OrdemServicoItemDAO.listarServico("");
        dimensionarTabela();
    }

    private void atualizar() {
        if (tblServico.getRowCount() > 0) {
            if (tblServico.getSelectedRow() > -1) {
                int linha = tblServico.getSelectedRow();
                int id = Integer.parseInt(tblServico.getValueAt(linha, 0).toString());

                FrmProprietarioVeiculo cliente = new FrmProprietarioVeiculo(id, "M");
                frmMenuPrincipal.JDesktopPanePrincipall.add(cliente);
                cliente.setVisible(true);
                cliente.setPosicao();
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione um cliente na tabela abaixo.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Não há registro na tabela para ser atualizado.");
        }
    }

    private void excluir() {
        if (tblServico.getRowCount() > 0) {
            if (tblServico.getSelectedRow() > -1) {
                int linha = tblServico.getSelectedRow();
                int id = Integer.parseInt(tblServico.getValueAt(linha, 0).toString());
                String nome = tblServico.getValueAt(linha, 1).toString();

                int ok = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja excluir"
                        + " cliente " + nome + "?", "Aviso", JOptionPane.YES_NO_OPTION);

                if (ok == 0) {
                    ClienteDAO.excluir(id);
                    ClienteDAO.listarCliente("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione o cliente na tabela abaixo para ser excluído.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Não há registro na tabela para ser excluído.");
        }
    }

    private void dimensionarTabela() {
        int altura = 20 * tblServico.getRowCount();
        tblServico.setPreferredSize(new java.awt.Dimension(610, altura));
    }

    public void setPosicao() {
        Dimension d = this.getDesktopPane().getSize();
        this.setLocation((d.width - this.getSize().width) / 2, (d.height - this.getSize().height) / 2);
    }

